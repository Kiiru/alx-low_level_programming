/**
 * add - Operation to add two integers
 * @a: integer first value
 * @b: integer second value
 * Return: sum of the two integers
*/
int add(int a, int b)
{
    int sum;

    sum = a + b;

    return (sum);
}

/**
 * sub - Operation to find the difference between two integers
 * @a: first integer value
 * @b: second integer value
 * Return: The difference between the integers
*/
int sub(int a, int b)
{
    int dif;

    dif = a - b;

    return (dif);
}

/**
 * mul - Multiplication operation
 * @a: first integer value
 * @b: second integer value
 * Return: Result
*/
int mul(int a, int b)
{
    return (a * b);
}

/**
 * div - Division operation
 * @a: first integer value
 * @b
*/
float div(int a, int b);

/**
 * mod
*/
int mod(int a, int b);